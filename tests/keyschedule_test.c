/* TLS 1.3 key-schedule KATs from RFC 8448 section 3 (SHA-256). */

#include "wolfnano_crypto.h"
#include "wn_keyschedule.h"
#include <stdio.h>

static int fails = 0;

static void check(int ok, const char* name)
{
    printf("%s %s\n", ok ? "PASS" : "FAIL", name);
    if (!ok) {
        fails++;
    }
}

static int eq(const byte* a, const byte* b, word32 n)
{
    return XMEMCMP(a, b, n) == 0;
}

int main(void)
{
    /* RFC 8448 section 3 */
    static const byte earlyExp[32] = {
        0x33,0xad,0x0a,0x1c,0x60,0x7e,0xc0,0x3b,0x09,0xe6,0xcd,0x98,0x93,0x68,0x0c,0xe2,
        0x10,0xad,0xf3,0x00,0xaa,0x1f,0x26,0x60,0xe1,0xb2,0x2e,0x10,0xf1,0x70,0xf9,0x2a
    };
    static const byte emptyHashExp[32] = {
        0xe3,0xb0,0xc4,0x42,0x98,0xfc,0x1c,0x14,0x9a,0xfb,0xf4,0xc8,0x99,0x6f,0xb9,0x24,
        0x27,0xae,0x41,0xe4,0x64,0x9b,0x93,0x4c,0xa4,0x95,0x99,0x1b,0x78,0x52,0xb8,0x55
    };
    static const byte derivedExp[32] = {
        0x6f,0x26,0x15,0xa1,0x08,0xc7,0x02,0xc5,0x67,0x8f,0x54,0xfc,0x9d,0xba,0xb6,0x97,
        0x16,0xc0,0x76,0x18,0x9c,0x48,0x25,0x0c,0xeb,0xea,0xc3,0x57,0x6c,0x36,0x11,0xba
    };
    /* ECDHE shared secret (IKM for the Handshake Secret). */
    static const byte ecdhe[32] = {
        0x8b,0xd4,0x05,0x4f,0xb5,0x5b,0x9d,0x63,0xfd,0xfb,0xac,0xf9,0xf0,0x4b,0x9f,0x0d,
        0x35,0xe6,0xd6,0x3f,0x53,0x75,0x63,0xef,0xd4,0x62,0x72,0x90,0x0f,0x89,0x49,0x2d
    };
    static const byte hsSecretExp[32] = {
        0x1d,0xc8,0x26,0xe9,0x36,0x06,0xaa,0x6f,0xdc,0x0a,0xad,0xc1,0x2f,0x74,0x1b,0x01,
        0x04,0x6a,0xa6,0xb9,0x9f,0x69,0x1e,0xd2,0x21,0xa9,0xf0,0xca,0x04,0x3f,0xbe,0xac
    };
    /* Transcript-Hash(ClientHello .. ServerHello). */
    static const byte chShHash[32] = {
        0x86,0x0c,0x06,0xed,0xc0,0x78,0x58,0xee,0x8e,0x78,0xf0,0xe7,0x42,0x8c,0x58,0xed,
        0xd6,0xb4,0x3f,0x2c,0xa3,0xe6,0xe9,0x5f,0x02,0xed,0x06,0x3c,0xf0,0xe1,0xca,0xd8
    };
    static const byte cHsTrafficExp[32] = {
        0xb3,0xed,0xdb,0x12,0x6e,0x06,0x7f,0x35,0xa7,0x80,0xb3,0xab,0xf4,0x5e,0x2d,0x8f,
        0x3b,0x1a,0x95,0x07,0x38,0xf5,0x2e,0x96,0x00,0x74,0x6a,0x0e,0x27,0xa5,0x5a,0x21
    };
    static const byte sHsTrafficExp[32] = {
        0xb6,0x7b,0x7d,0x69,0x0c,0xc1,0x6c,0x4e,0x75,0xe5,0x42,0x13,0xcb,0x2d,0x37,0xb4,
        0xe9,0xc9,0x12,0xbc,0xde,0xd9,0x10,0x5d,0x42,0xbe,0xfd,0x59,0xd3,0x91,0xad,0x38
    };
    byte psk[32];
    byte early[32];
    byte emptyHash[32];
    byte derived[32];
    byte hsSecret[32];
    byte cHs[32];
    byte sHs[32];
    int rc;

    printf("wolfNanoTLS TLS 1.3 key-schedule KATs (RFC 8448 section 3)\n");

    XMEMSET(psk, 0, sizeof(psk));

    rc = wn_Tls13_Extract(early, NULL, 0, psk, sizeof(psk), WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(early, earlyExp, 32),
          "Early Secret = HKDF-Extract(0, 0)");

    rc = wc_Sha256Hash((const byte*)"", 0, emptyHash);
    check((rc == 0) && eq(emptyHash, emptyHashExp, 32),
          "Transcript-Hash(\"\") = SHA-256(\"\")");

    rc = wn_Tls13_DeriveSecret(derived, early, "derived", emptyHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(derived, derivedExp, 32),
          "Derive-Secret(Early, \"derived\", \"\")");

    rc = wn_Tls13_Extract(hsSecret, derived, 32, ecdhe, sizeof(ecdhe),
                          WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(hsSecret, hsSecretExp, 32),
          "Handshake Secret = HKDF-Extract(derived, ECDHE)");

    rc = wn_Tls13_DeriveSecret(cHs, hsSecret, "c hs traffic", chShHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(cHs, cHsTrafficExp, 32),
          "client_handshake_traffic_secret");

    rc = wn_Tls13_DeriveSecret(sHs, hsSecret, "s hs traffic", chShHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(sHs, sHsTrafficExp, 32),
          "server_handshake_traffic_secret");

    printf("\n%s (%d failure%s)\n", fails ? "FAILED" : "ALL PASS",
           fails, fails == 1 ? "" : "s");
    return fails ? 1 : 0;
}
