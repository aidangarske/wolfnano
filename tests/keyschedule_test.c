/* TLS 1.3 key-schedule KATs from RFC 8448 section 3 (SHA-256). */

#include "wolfnano_crypto.h"
#include "wn_keyschedule.h"
#include <stdio.h>

static int fails = 0;

static void check(int ok, const char* name)
{
    printf("%s %s\n", ok ? "\033[32mPASS\033[0m" : "\033[31mFAIL\033[0m", name);
    if (!ok) {
        fails++;
    }
}

static int eq(const byte* a, const byte* b, word32 n)
{
    return XMEMCMP(a, b, n) == 0;
}

int main(void)
{
    /* RFC 8448 section 3 */
    static const byte earlyExp[32] = {
        0x33,0xad,0x0a,0x1c,0x60,0x7e,0xc0,0x3b,0x09,0xe6,0xcd,0x98,0x93,0x68,0x0c,0xe2,
        0x10,0xad,0xf3,0x00,0xaa,0x1f,0x26,0x60,0xe1,0xb2,0x2e,0x10,0xf1,0x70,0xf9,0x2a
    };
    static const byte emptyHashExp[32] = {
        0xe3,0xb0,0xc4,0x42,0x98,0xfc,0x1c,0x14,0x9a,0xfb,0xf4,0xc8,0x99,0x6f,0xb9,0x24,
        0x27,0xae,0x41,0xe4,0x64,0x9b,0x93,0x4c,0xa4,0x95,0x99,0x1b,0x78,0x52,0xb8,0x55
    };
    static const byte derivedExp[32] = {
        0x6f,0x26,0x15,0xa1,0x08,0xc7,0x02,0xc5,0x67,0x8f,0x54,0xfc,0x9d,0xba,0xb6,0x97,
        0x16,0xc0,0x76,0x18,0x9c,0x48,0x25,0x0c,0xeb,0xea,0xc3,0x57,0x6c,0x36,0x11,0xba
    };
    /* ECDHE shared secret (IKM for the Handshake Secret). */
    static const byte ecdhe[32] = {
        0x8b,0xd4,0x05,0x4f,0xb5,0x5b,0x9d,0x63,0xfd,0xfb,0xac,0xf9,0xf0,0x4b,0x9f,0x0d,
        0x35,0xe6,0xd6,0x3f,0x53,0x75,0x63,0xef,0xd4,0x62,0x72,0x90,0x0f,0x89,0x49,0x2d
    };
    static const byte hsSecretExp[32] = {
        0x1d,0xc8,0x26,0xe9,0x36,0x06,0xaa,0x6f,0xdc,0x0a,0xad,0xc1,0x2f,0x74,0x1b,0x01,
        0x04,0x6a,0xa6,0xb9,0x9f,0x69,0x1e,0xd2,0x21,0xa9,0xf0,0xca,0x04,0x3f,0xbe,0xac
    };
    /* Transcript-Hash(ClientHello .. ServerHello). */
    static const byte chShHash[32] = {
        0x86,0x0c,0x06,0xed,0xc0,0x78,0x58,0xee,0x8e,0x78,0xf0,0xe7,0x42,0x8c,0x58,0xed,
        0xd6,0xb4,0x3f,0x2c,0xa3,0xe6,0xe9,0x5f,0x02,0xed,0x06,0x3c,0xf0,0xe1,0xca,0xd8
    };
    static const byte cHsTrafficExp[32] = {
        0xb3,0xed,0xdb,0x12,0x6e,0x06,0x7f,0x35,0xa7,0x80,0xb3,0xab,0xf4,0x5e,0x2d,0x8f,
        0x3b,0x1a,0x95,0x07,0x38,0xf5,0x2e,0x96,0x00,0x74,0x6a,0x0e,0x27,0xa5,0x5a,0x21
    };
    static const byte sHsTrafficExp[32] = {
        0xb6,0x7b,0x7d,0x69,0x0c,0xc1,0x6c,0x4e,0x75,0xe5,0x42,0x13,0xcb,0x2d,0x37,0xb4,
        0xe9,0xc9,0x12,0xbc,0xde,0xd9,0x10,0x5d,0x42,0xbe,0xfd,0x59,0xd3,0x91,0xad,0x38
    };
    /* server handshake write key/iv and finished key (RFC 8448 section 3). */
    static const byte sWriteKeyExp[16] = {
        0x3f,0xce,0x51,0x60,0x09,0xc2,0x17,0x27,0xd0,0xf2,0xe4,0xe8,0x6e,0xe4,0x03,0xbc
    };
    static const byte sWriteIvExp[12] = {
        0x5d,0x31,0x3e,0xb2,0x67,0x12,0x76,0xee,0x13,0x00,0x0b,0x30
    };
    static const byte sFinishedKeyExp[32] = {
        0x00,0x8d,0x3b,0x66,0xf8,0x16,0xea,0x55,0x9f,0x96,0xb5,0x37,0xe8,0x85,0xc3,0x1f,
        0xc0,0x68,0xbf,0x49,0x2c,0x65,0x2f,0x01,0xf2,0x88,0xa1,0xd8,0xcd,0xc1,0x9f,0xc8
    };
    /* Master Secret and application traffic secrets (RFC 8448 section 3). */
    static const byte masterExp[32] = {
        0x18,0xdf,0x06,0x84,0x3d,0x13,0xa0,0x8b,0xf2,0xa4,0x49,0x84,0x4c,0x5f,0x8a,0x47,
        0x80,0x01,0xbc,0x4d,0x4c,0x62,0x79,0x84,0xd5,0xa4,0x1d,0xa8,0xd0,0x40,0x29,0x19
    };
    /* Transcript-Hash(ClientHello .. server Finished). */
    static const byte chSfHash[32] = {
        0x96,0x08,0x10,0x2a,0x0f,0x1c,0xcc,0x6d,0xb6,0x25,0x0b,0x7b,0x7e,0x41,0x7b,0x1a,
        0x00,0x0e,0xaa,0xda,0x3d,0xaa,0xe4,0x77,0x7a,0x76,0x86,0xc9,0xff,0x83,0xdf,0x13
    };
    static const byte cApTrafficExp[32] = {
        0x9e,0x40,0x64,0x6c,0xe7,0x9a,0x7f,0x9d,0xc0,0x5a,0xf8,0x88,0x9b,0xce,0x65,0x52,
        0x87,0x5a,0xfa,0x0b,0x06,0xdf,0x00,0x87,0xf7,0x92,0xeb,0xb7,0xc1,0x75,0x04,0xa5
    };
    static const byte sApTrafficExp[32] = {
        0xa1,0x1a,0xf9,0xf0,0x55,0x31,0xf8,0x56,0xad,0x47,0x11,0x6b,0x45,0xa9,0x50,0x32,
        0x82,0x04,0xb4,0xf4,0x4b,0xfb,0x6b,0x3a,0x4b,0x4f,0x1f,0x3f,0xcb,0x63,0x16,0x43
    };
    byte psk[32];
    byte early[32];
    byte emptyHash[32];
    byte derived[32];
    byte hsSecret[32];
    byte cHs[32];
    byte sHs[32];
    byte sKey[16];
    byte sIv[12];
    byte sFin[32];
    byte derived2[32];
    byte master[32];
    byte cAp[32];
    byte sAp[32];
    byte fmac[32];
    byte fmanual[32];
    Hmac h;
    int rc;

    printf("wolfNanoTLS TLS 1.3 key-schedule KATs (RFC 8448 section 3)\n");

    XMEMSET(psk, 0, sizeof(psk));

    rc = wn_Tls13_Extract(early, NULL, 0, psk, sizeof(psk), WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(early, earlyExp, 32),
          "Early Secret = HKDF-Extract(0, 0)");

    rc = wc_Sha256Hash((const byte*)"", 0, emptyHash);
    check((rc == 0) && eq(emptyHash, emptyHashExp, 32),
          "Transcript-Hash(\"\") = SHA-256(\"\")");

    rc = wn_Tls13_DeriveSecret(derived, early, "derived", emptyHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(derived, derivedExp, 32),
          "Derive-Secret(Early, \"derived\", \"\")");

    rc = wn_Tls13_Extract(hsSecret, derived, 32, ecdhe, sizeof(ecdhe),
                          WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(hsSecret, hsSecretExp, 32),
          "Handshake Secret = HKDF-Extract(derived, ECDHE)");

    rc = wn_Tls13_DeriveSecret(cHs, hsSecret, "c hs traffic", chShHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(cHs, cHsTrafficExp, 32),
          "client_handshake_traffic_secret");

    rc = wn_Tls13_DeriveSecret(sHs, hsSecret, "s hs traffic", chShHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(sHs, sHsTrafficExp, 32),
          "server_handshake_traffic_secret");

    rc = wn_Tls13_ExpandLabel(sKey, sizeof(sKey), sHs, "key", NULL, 0,
                              WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(sKey, sWriteKeyExp, 16),
          "server write key = HKDF-Expand-Label(.., \"key\", \"\")");

    rc = wn_Tls13_ExpandLabel(sIv, sizeof(sIv), sHs, "iv", NULL, 0, WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(sIv, sWriteIvExp, 12),
          "server write iv = HKDF-Expand-Label(.., \"iv\", \"\")");

    rc = wn_Tls13_ExpandLabel(sFin, sizeof(sFin), sHs, "finished", NULL, 0,
                              WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(sFin, sFinishedKeyExp, 32),
          "server finished key = HKDF-Expand-Label(.., \"finished\", \"\")");

    rc  = wn_Tls13_DeriveSecret(derived2, hsSecret, "derived", emptyHash, 32,
                                WC_SHA256);
    rc |= wn_Tls13_Extract(master, derived2, 32, psk, sizeof(psk), WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(master, masterExp, 32),
          "Master Secret = HKDF-Extract(derived, 0)");

    rc = wn_Tls13_DeriveSecret(cAp, master, "c ap traffic", chSfHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(cAp, cApTrafficExp, 32),
          "client_application_traffic_secret_0");

    rc = wn_Tls13_DeriveSecret(sAp, master, "s ap traffic", chSfHash, 32,
                               WC_SHA256);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(sAp, sApTrafficExp, 32),
          "server_application_traffic_secret_0");

    /* FinishedMac == HMAC(HKDF-Expand-Label(sHs,"finished"), transcript).
     * The finished key itself is RFC 8448 KAT'd above (sFinishedKeyExp); here
     * we confirm FinishedMac composes it with HMAC correctly. */
    rc  = wn_Tls13_FinishedMac(fmac, sHs, emptyHash, 32, WC_SHA256);
    rc |= wc_HmacInit(&h, NULL, INVALID_DEVID);
    rc |= wc_HmacSetKey(&h, WC_SHA256, sFinishedKeyExp, 32);
    rc |= wc_HmacUpdate(&h, emptyHash, 32);
    rc |= wc_HmacFinal(&h, fmanual);
    wc_HmacFree(&h);
    check((rc == WOLFNANOTLS_SUCCESS) && eq(fmac, fmanual, 32),
          "FinishedMac = HMAC(finished_key, transcript)");

    printf("\n%s (%d failure%s)\n", fails ? "\033[31mFAILED\033[0m" : "\033[32mALL PASS\033[0m",
           fails, fails == 1 ? "" : "s");
    return fails ? 1 : 0;
}
